                    <p>The house was three miles from the station, but, before the dusty hired hack had rattled along
                        for five minutes, the children began to put their heads out of the carriage window and say,
                        "Aren't we nearly there?" And every time they passed a house, which was not very often, they all
                        said, "Oh, is this it?" But it never was, till they reached the very top of the hill, just past
                        the chalk-quarry and before you come to the gravel-pit. And then there was a white house with a
                        green garden and an orchard beyond, and mother said, "Here we are!"</p>
                    <p>"How white the house is," said Robert.</p>
                    <p>"And look at the roses," said Anthea.</p>
                    <p>"And the plums," said Jane.</p>
                    <p>"It is rather decent," Cyril admitted.</p>
                    <p>The Baby said, "Wanty go walky;" and the hack stopped with a last rattle and jolt.</p>
                    <p>Everyone got its legs kicked or its feet trodden on in the scramble to get out of the carriage
                        that very minute, but no one seemed to mind. Mother, curiously enough, was in no hurry to get
                        out; and even when she had come down slowly and by the step, and with no jump at all, she seemed
                        to wish to see the boxes carried in, and even to pay the driver, instead of joining in that
                        first glorious rush round the garden and orchard and the thorny, thistly, briery, brambly
                        wilderness beyond the broken gate and the dry fountain at the side of the house. But the
                        children were wiser, for once. It was not really a pretty house at all; it was quite ordinary,
                        and mother thought it was rather inconvenient, and was quite annoyed at there being no shelves,
                        to speak of, and hardly a cupboard in the place. Father used to say that the iron-work on the
                        roof and coping was like an architect's nightmare. But the house was deep in the country, with
                        no other house in sight, and the children had been in London for two years, without so much as
                        once going to the seaside even for a day by an excursion train, and so the White House seemed to
                        them a sort of Fairy Palace set down in an Earthly Paradise. For London is like prison for
                        children, especially if their relations are not rich.</p>
                    <p>Of course there are the shops and theatres, and entertainments and things, but if your people are
                        rather poor you don't get taken to the theatres, and you can't buy things out of the shops; and
                        London has none of those nice things that children may play with without hurting the things or
                        themselves--such as trees and sand and woods and waters. And nearly everything in London is the
                        wrong sort of shape--all straight lines and flat streets, instead of being all sorts of odd
                        shapes, like things are in the country. Trees are all different, as you know, and I am sure some
                        tiresome person must have told you that there are no two blades of grass exactly alike. But in
                        streets, where the blades of grass don't grow, everything is like everything else. This is why
                        many children who live in the towns are so extremely naughty. They do not know what is the
                        matter with them, and no more do their fathers and mothers, aunts, uncles, cousins, tutors,
                        governesses, and nurses; but I know. And so do you, now. Children in the country are naughty
                        sometimes, too, but that is for quite different reasons.</p>
                    <p>The children had explored the gardens and the outhouses thoroughly before they were caught and
                        cleaned for tea, and they saw quite well that they were certain to be happy at the White House.
                        They thought so from the first moment, but when they found the back of the house covered with
                        jasmine, all in white flower, and smelling like a bottle of the most expensive perfume that is
                        ever given for a birthday present; and when they had seen the lawn, all green and smooth, and
                        quite different from the brown grass in the gardens at Camden Town; and when they found the
                        stable with a loft over it and some old hay still left, they were almost certain;and when Robert
                        had found the broken swing and tumbled out of it and got a bump on his head the size of an egg,
                        and Cyril had nipped his finger in the door of a hutch that seemed made to keep rabbits in, if
                        you ever had any, they had no longer any doubts whatever.</p>
                    <p>The best part of it all was that there were no rules about not going to places and not doing
                        things. In London almost everything is labelled "You mustn't touch," and though the label is
                        invisible it's just as bad, because you know it's there, or if you don't you very soon get told.
                    </p>
                    <p>The White House was on the edge of a hill, with a wood behind it--and the chalk-quarry on one
                        side and the gravel-pit on the other. Down at the bottom of the hill was a level plain, with
                        queer-shaped white buildings where people burnt lime, and a big red brewery and other houses;
                        and when the big chimneys were smoking and the sun was setting, the valley looked as if it was
                        filled with golden mist, and the limekilns and hop-drying houses glimmered and glittered till
                        they were like an enchanted city out of the Arabian Nights.</p>
                    <p>It was at the gravel-pits. Father had to go away suddenly on business, and mother had gone away
                        to stay with Granny, who was not very well. They both went in a great hurry, and when they were
                        gone the house seemed dreadfully quiet and empty, and the children wandered from one room to
                        another and looked at the bits of paper and string on the floors left over from the packing, and
                        not yet cleared up, and wished they had something to do. It was Cyril who saidâ€”</p>
                    <p>"I say, let's take our spades and dig in the gravel-pits. We can pretend it's seaside."</p>
                    <p>"Father says it was once," Anthea said; "he says there are shells there thousands of years old."
                    </p>
                    <p>So they went. Of course they had been to the edge of the gravel-pit and looked over, but they had
                        not gone down into it for fear father should say they mustn't play there, and it was the same
                        with the chalk-quarry. The gravel-pit is not really dangerous if you don't try to climb down the
                        edges, but go the slow safe way round by the road, as if you were a cart.</p>
                    <p>Each of the children carried its own spade, and took it in turns to carry the Lamb. He was the
                        baby, and they called him that because "Baa" was the first thing he ever said. They called
                        Anthea "Panther," which seems silly when you read it, but when you say it it sounds a little
                        like her name. </p>
                    <p>The gravel-pit is very large and wide, with grass growing round the edges at the top, and dry
                        stringy wildflowers, purple and yellow. It is like a giant's washbowl. And there are mounds of
                        gravel, and holes in the sides of the bowl where gravel has been taken out, and high up in the
                        steep sides there are the little holes that are the little front doors of the little
                        bank-martins' little houses.</p>
